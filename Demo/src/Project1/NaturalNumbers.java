package Project1;

public class NaturalNumbers {

	public static void main(String[] args) {
		//to find first 10 natural numbers
		int Sum_result = 0;
		
		for (int k=1;k<=10;k++) {
			Sum_result += k;
			System.out.println(Sum_result);
		}
		System.out.println("Sum of the first 10 natural numbers are : " + 	Sum_result);

	}

}
